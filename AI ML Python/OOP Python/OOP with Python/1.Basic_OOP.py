"""OOP (Object-Oriented Programming) is a programming style where
 we organize code using objects (data + functions together)."""

"""Why we need OOP?
👉 We need OOP to:
Keep code clean and organized
Avoid repeating the same code
Handle big programs easily"""

"""🔹 What is a Class?

👉 Class = blueprint/template
It defines what data and actions an object will have.

🔹 What is an Object?

👉 Object = real instance of a class
It is the actual thing created using the class."""

"""In OOP, data's are called attributes and functions are called methods."""
#Why OOP is different from normal programming?
"""In normal programming, we write code in a linear way,
 and we often end up repeating the same code multiple times. 
 In OOP, we can create classes that serve as blueprints for objects, 
 allowing us to reuse code and keep it organized. 
 This makes it easier to manage larger programs and reduces the chances of errors."""